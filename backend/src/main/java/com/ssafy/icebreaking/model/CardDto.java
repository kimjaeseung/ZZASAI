package com.ssafy.icebreaking.model;

public class CardDto {

	private String cardname;
	private String cardurl_front;
	private String cardurl_back;
	
	public String getCardname() {
		return cardname;
	}
	public void setCardname(String cardname) {
		this.cardname = cardname;
	}
	public String getCardurl_front() {
		return cardurl_front;
	}
	public void setCardurl_front(String cardurl_front) {
		this.cardurl_front = cardurl_front;
	}
	public String getCardurl_back() {
		return cardurl_back;
	}
	public void setCardurl_back(String cardurl_back) {
		this.cardurl_back = cardurl_back;
	}

	
	
}
